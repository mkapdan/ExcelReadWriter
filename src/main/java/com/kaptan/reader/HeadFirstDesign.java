package com.kaptan.reader;

public class HeadFirstDesign {

	private String head;

	private String first;

	private String design;

	public HeadFirstDesign() {
		super();
	}

	private String getHead() {
		return head;
	}

	private void setHead(String head) {
		this.head = head;
	}

	private String getFirst() {
		return first;
	}

	private void setFirst(String first) {
		this.first = first;
	}

	private String getDesign() {
		return design;
	}

	private void setDesign(String design) {
		this.design = design;
	}

}
